public class Triangle
{
    public int MinimumTotal(IList<IList<int>> triangle)
    {
        if (triangle.Count == 1) return triangle[0][0];

        var buffer = triangle.Select(t => new int[t.Count]).ToArray();
        foreach (var b in buffer)
        {
            for (var i = 0; i < b.Length; i++)
            {
                b[i] = int.MaxValue;
            }
        }

        buffer[0][0] = triangle[0][0];

        for (var y = 1; y < triangle.Count; y++)
        {
            for (int x = 0; x < triangle[y].Count; x++)
            {
                if (x - 1 >= 0 && x < buffer[y - 1].Length)
                {
                    buffer[y][x] = Math.Min(triangle[y][x] + buffer[y - 1][x], triangle[y][x] + buffer[y - 1][x - 1]);
                }
                else if (x >= buffer[y - 1].Length)
                {
                    buffer[y][x] = triangle[y][x] + buffer[y - 1][x - 1];
                }
                else
                {
                    buffer[y][x] = triangle[y][x] + buffer[y - 1][x];
                }
            }
        }

        return buffer[^1].Min();
    }
}

