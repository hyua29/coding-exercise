namespace LeetCode.LeetCode150;

public class Triangle
{
    public int HasReachedEnd = 0;
    public int MinimumTotal(IList<IList<int>> triangle)
    {
        var result = int.MaxValue;

        var depth = triangle.Count;

        AddPoint(1, 0, 0, depth, triangle, 0, ref result);
        return result;
    }

    private void AddPoint(int currentDepth, int y, int x, int maxDepth, IList<IList<int>> triangle,
        int currentSum, ref int result)
    {
        var value = triangle[y][x];
        currentSum += value;

        if (currentDepth == maxDepth)
        {
            result = Math.Min(currentSum, result);
            HasReachedEnd++;
            return;
        }

        AddPoint(currentDepth + 1, y + 1, x, maxDepth, triangle, currentSum, ref result);
        AddPoint(currentDepth + 1, y + 1, x + 1, maxDepth, triangle, currentSum, ref result);
    }
}

